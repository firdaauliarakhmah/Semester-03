public class Employee {
    private int nomor;
    private String nama;
    private String divisi;
    private double gaji;

    public Employee(int nomor, String nama, String divisi, double gaji) { //constractor
        this.nomor = nomor;
        this.nama = nama;
        this.divisi = divisi;
        this.gaji = gaji;
    }

    public double getGaji() {
        return gaji;
    }

    public void setGaji(double gaji) {
        this.gaji = gaji;
    }

    public int getNomor() {
        return nomor;
    }

    public void setNomor(int nomor) {
        this.nomor = nomor;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getDivisi() {
        return divisi;
    }

    public void setDivisi(String divisi) {
        this.divisi = divisi;
    }
    
}
